-- Notes:
-- > nvim_set_keymap example is wrong in :h? Should not be string in options.
function Toggle_cursor_centering()
    if vim.opt.scrolloff:get() == 999 then
        vim.opt.scrolloff = 5
    else
        vim.opt.scrolloff = 999
    end
end


